package exercicio5;

public class Patinete {
	
	private String corPatinete;
	private int quantidadeRodas;
	private boolean eletronico;
	
	public String getCorPatinete() {
		return corPatinete;
	}
	public void setCorPatinete(String corPatinete) {
		this.corPatinete = corPatinete;
	}
	public int getQuantidadeRodas() {
		return quantidadeRodas;
	}
	public void setQuantidadeRodas(int quantidadeRodas) {
		this.quantidadeRodas = quantidadeRodas;
	}
	public boolean isEletronico() {
		return eletronico;
	}
	public void setEletronico(boolean eletronico) {
		this.eletronico = eletronico;
	}
	
	

}
